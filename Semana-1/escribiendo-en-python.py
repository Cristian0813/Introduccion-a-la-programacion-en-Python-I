# Pregunta
# Imagina que asigno a la variable x el valor 0 (x=0) y a la variable y el valor 2 (y=2).
# Luego asigno a la variable res la suma de x más y (res=x+y).
# Luego cambio el valor de x asignándole el valor 6 y repito la suma anterior (res=x+y).
# ¿Cuánto dará el valor de la variable res al final?
# 
# >>> x = 6
# >>> y = 2
# >>> res = x + y
# >>> res
# >>> 8
# Correcto
# ¡Excelente! El valor de la variable res es la suma del último valor que se le dio a la variable x (es decir, 6) más el valor que tenía la variable y (es decir, 2). La suma de 6+2 es igual a 8.